DECLARE @VAR1 VARCHAR(100),
@IDADE INT;
--ATRIBUIR NA VARIAVEL

SET @VAR1 ='JAO';
SET @IDADE = 24;

--EXIBIR
SELECT @VAR1 + ' ' + CAST (@IDADE AS VARCHAR(10));
PRINT @IDADE;

-- IF
IF @IDADE < 24
BEGIN
SELECT 'MENOR';
END

ELSE
BEGIN
SELECT 'MAIOR';
END

-- WHILE
DECLARE @NUMERO AS INT;
SET @NUMERO = 0;
WHILE(@NUMERO < 10)
BEGIN
PRINT @NUMERO;
SET @NUMERO += 1;
END


SELECT
CASE @IDADE
WHEN 15 THEN 'IDADE 15'
WHEN 16 THEN 'IDADE 16'
WHEN 17 THEN 'IDADE 17'
